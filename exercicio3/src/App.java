public class App {
    public static void main(String[] args){
        int tamanho = 10;         
        int tabuada = 6;
       
        for(int i = 1; i <= tamanho; i++){
            int element = i * tabuada;
            System.out.println(tabuada + " * " + i + " = " + element);
                        
        }
       
}
}
